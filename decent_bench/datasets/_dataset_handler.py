from __future__ import annotations

from abc import ABC, abstractmethod
from collections.abc import Sequence
from numbers import Integral
from typing import Any

from decent_bench.utils.types import Dataset


class DatasetHandler(ABC):
    """
    Abstract wrapper for datasets used in decentralized optimization benchmark problems.

    This class provides an interface for loading datasets and materializing index
    partitions for decentralized optimization scenarios.

    In decentralized optimization, the dataset is typically divided among multiple
    agents in a network, where each agent has access to only a subset of the complete
    dataset. Partitioning utilities decide which indices belong to each agent, while
    this class maps those indices to actual datapoints.

    When defining benchmark problems, a DatasetHandler instance can be used to:

    - Provide local datasets to each agent in the network via :meth:`split`
    - Define the overall optimization problem (e.g., empirical risk minimization)
    - Serve as a test set for evaluating decentralized algorithms on the full dataset
      (e.g. via :meth:`get_datapoints`) by defining the `test_data` field of
      :class:`~decent_bench.benchmark.BenchmarkProblem`.

    Data Structure:
        The dataset consists of datapoints, where each datapoint is a tuple of
        (features, targets). Features and targets are represented as :class:`~decent_bench.utils.array.Array`
        objects or framework-specific tensor objects in special cases. For unsupervised learning,
        targets are usually None. Partitions are sequences of such datapoints,
        allowing users to easily distribute local datasets among agents.

    Note:
        Implementations may load data from various sources (files, generators,
        synthetic data, etc) and are not required to store all datapoints in memory.

    """

    @property
    @abstractmethod
    def n_samples(self) -> int:
        """Total number of datapoints in the dataset."""

    @property
    @abstractmethod
    def n_features(self) -> int:
        """Number of feature dimensions."""

    @property
    @abstractmethod
    def n_targets(self) -> int:
        """Number of target dimensions."""

    @abstractmethod
    def get_datapoints(self) -> Dataset:
        """
        Return all datapoints in the dataset.

        Can be used for evaluation on the full dataset or creation of test datasets.
        """

    def get_labels(self) -> list[Any]:
        """
        Return one target label per datapoint.

        Dataset handlers can override this method to avoid materializing the full
        dataset when labels are available directly from the underlying source.
        """
        return [target for _, target in self.get_datapoints()]

    @abstractmethod
    def split(
        self,
        n_partitions: int | None = None,
        *,
        partitions: Sequence[Sequence[int]] | None = None,
    ) -> Sequence[Dataset]:
        """
        Materialize dataset partitions for distribution among agents.

        Pass explicit index partitions generated by a function from
        ``decent_bench.datasets.partitioners``. When only ``n_partitions`` is
        provided, the dataset is split IID at random.

        Args:
            n_partitions: Number of IID partitions to create when ``partitions`` is omitted.
                Defaults to one.
            partitions: Explicit index partitions to materialize. Indices must be
                unique and in bounds, but full dataset coverage is not required.

        """

    def _resolve_partitions(
        self,
        n_partitions: int | None,
        partitions: Sequence[Sequence[int]] | None,
    ) -> list[list[int]]:
        if n_partitions is not None and partitions is not None:
            raise ValueError("Only one of n_partitions and partitions can be provided")
        if partitions is None:
            from .partitioners import split_iid  # noqa: PLC0415

            return split_iid(self, n_partitions=1 if n_partitions is None else n_partitions)

        resolved: list[list[int]] = []
        seen_indices: set[int] = set()
        for partition in partitions:
            resolved_partition: list[int] = []
            for index in partition:
                if isinstance(index, bool) or not isinstance(index, Integral):
                    raise TypeError("Partition indices must be integers")
                normalized_index = int(index)
                if normalized_index < 0 or normalized_index >= len(self):
                    raise IndexError(
                        f"Partition index {normalized_index} is out of bounds for dataset of size {len(self)}"
                    )
                if normalized_index in seen_indices:
                    raise ValueError(f"Partition index {normalized_index} occurs more than once")
                seen_indices.add(normalized_index)
                resolved_partition.append(normalized_index)
            resolved.append(resolved_partition)
        return resolved

    def __len__(self) -> int:
        """Return the number of datapoints in the dataset."""
        return self.n_samples
